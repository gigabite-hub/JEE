                          <p> If the above applies to your organization, you must comply with the <a style="color: blue;font-size: 16px;"  href="https://www.ontario.ca/page/accessibility-laws"  target="_blank" class="LINK3">Accessibility for Ontarians with Disabilities Act (AODA)</a> and all of its accessibility requirements by law.  Over <a style="color: blue;font-size: 16px;" href="https://www.ontario.ca/page/accessibility-rules-businesses-and-non-profits"   target="_blank" class="LINK3">385,000 businesses</a> in Ontario must comply.</p>
                          <p> Full-time, part-time, consultants, volunteers, and contract staff must complete mandatory <a style="color: blue; font-size: 16px;"  href="https://www.ontario.ca/page/how-make-customer-service-accessible" target="_blank" class="LINK3">customer service training</a> to properly accommodate Ontarians who live with visible (physical) and/or invisible (hearing, vision, speech, learning, cognitive, mental) disabilities.</p>
                          <p> The AODA requirements go beyond accessible "customer service training, ramps and wheelchairs." You must also ensure that Ontarians with disabilities have fair and equitable access to <a style="color: blue; font-size: 16px;"   href="https://www.ontario.ca/page/how-make-information-accessible" target="_blank" class="LINK3">information, communication, employment</a>, and websites (WCAG 2.0 Level AA) in accessible formats.</p>
